<?php

function soma($x, $y){
    $resultado = $x + $y;
    return $resultado;
    
}

function subtracao($x, $y){
    $resultado = $x - $y;
    return $resultado;
}

function multiplicacao($x, $y){
    $resultado = $x * $y;
    return $resultado;
}

function divisao($x, $y){
    $resultado = $x/$y;
    return $resultado;
}


?>